(function() {
  if (window.__influencesInit) return;
  window.__influencesInit = true;

  var influences = [
    ['Douglas Hofstadter', 'Self-reference as the mechanism by which meaning arises from formal systems.'],
    ['Carl Jung', 'The process of becoming whole through what you\'d rather not look at.'],
    ['Karl Friston', 'Every living system maintains itself by minimizing the distance between what it expects and what it encounters.'],
    ['Niklas Luhmann', 'How operationally closed systems stay coherent in overwhelming environments. Proved it with seventy books and a box of index cards.'],
    ['Ibn Khaldun', 'Behavioral observation as structural analysis, written from inside the collapse.'],
    ['Christopher Alexander', 'Good structure isn\'t designed from above but generated by patterns that already know how to produce life.'],
    ['Norbert Wiener', 'Cybernetics, and what happens when the feedback loop includes a human being.'],
    ['Michel Foucault', 'Power operates through observation and classification, not force. Then everyone built the systems he warned about.'],
    ['Baruch Spinoza', 'Freedom as adequate understanding of what constrains you.'],
    ['Marcus Aurelius', 'Governed an empire while writing private notes about governing himself. The notes lasted longer.'],
    ['Thomas Aquinas', 'Decisions belong at the lowest level competent to make them.'],
    ['Augustine', 'Fought his way into a framework he didn\'t inherit and wrote the first honest autobiography.'],
    ['Peter Turchin', 'Elite overproduction precedes instability with uncomfortable regularity.'],
    ['Isaac Asimov', 'Imagined a science of historical prediction and then showed why it breaks when a single unpredictable actor appears.'],
    ['Andrej Karpathy', 'Thinks before coding, ships simple things, and explains hard problems like someone who actually understands them.'],
    ['Paul Graham', 'Writes clearly about building things, which turns out to be harder than building things.'],
    ['Jiang Xueqin', 'Overlays the 1930s on the present and asks students what rhymes.']
  ];

  // Build modal DOM
  var overlay = document.createElement('div');
  overlay.id = 'influences-overlay';
  overlay.innerHTML =
    '<div class="influences-panel">' +
      '<div class="influences-header">' +
        '<span class="influences-title">Influences</span>' +
        '<button class="influences-close">&times;</button>' +
      '</div>' +
      '<div class="influences-scroll">' +
        influences.map(function(i) {
          return '<div class="influence-row">' +
            '<span class="influence-name">' + i[0] + '</span>' +
            '<span class="influence-note">' + i[1] + '</span>' +
          '</div>';
        }).join('') +
      '</div>' +
    '</div>';

  // Styles
  var style = document.createElement('style');
  style.textContent =
    '#influences-overlay{position:fixed;inset:0;z-index:9999;background:rgba(5,5,5,0.92);display:none;align-items:center;justify-content:center;backdrop-filter:blur(4px)}' +
    '#influences-overlay.open{display:flex}' +
    '.influences-panel{background:#0a0a0a;border:1px solid #b8965a;max-width:520px;width:90%;max-height:80vh;display:flex;flex-direction:column}' +
    '.influences-header{display:flex;justify-content:space-between;align-items:center;padding:0.75rem 1rem;border-bottom:1px solid rgba(184,150,90,0.2)}' +
    '.influences-title{font-family:"JetBrains Mono",monospace;font-size:0.7rem;color:#b8965a;text-transform:uppercase;letter-spacing:0.1em}' +
    '.influences-close{background:none;border:none;color:#b8965a;font-size:1.2rem;cursor:pointer;padding:0 0.25rem;line-height:1}' +
    '.influences-close:hover{color:#F2E6D0}' +
    '.influences-scroll{overflow-y:auto;padding:0.75rem 1rem;flex:1}' +
    '.influence-row{padding:0.5rem 0;border-bottom:1px solid rgba(255,255,255,0.04)}' +
    '.influence-row:last-child{border-bottom:none}' +
    '.influence-name{display:block;font-family:"Cormorant Garamond",Georgia,serif;font-size:0.95rem;color:#b8965a;margin-bottom:0.15rem}' +
    '.influence-note{display:block;font-family:Georgia,serif;font-size:0.8rem;color:rgba(232,220,200,0.5);line-height:1.4}' +
    '.influences-scroll::-webkit-scrollbar{width:4px}' +
    '.influences-scroll::-webkit-scrollbar-track{background:transparent}' +
    '.influences-scroll::-webkit-scrollbar-thumb{background:rgba(184,150,90,0.3);border-radius:2px}';

  document.head.appendChild(style);
  document.body.appendChild(overlay);

  function open() { overlay.classList.add('open'); }
  function close() { overlay.classList.remove('open'); }

  overlay.querySelector('.influences-close').addEventListener('click', close);
  overlay.addEventListener('click', function(e) {
    if (e.target === overlay) close();
  });
  document.addEventListener('keydown', function(e) {
    if (e.key === 'Escape') close();
  });

  // Expose globally so triggers can call it
  window.__openInfluences = open;

  // Auto-bind any trigger links
  document.querySelectorAll('[data-influences]').forEach(function(el) {
    el.style.cursor = 'pointer';
    el.addEventListener('click', function(e) { e.preventDefault(); open(); });
  });
})();
